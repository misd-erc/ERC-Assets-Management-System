const ContractManagement = () => {
  // 芒鈥濃偓芒鈥濃偓 Hooks 芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓
  const {
    vendors,
    searchQuery: vendorSearch,
    fetchVendors,
    loading: VendorLoading,
  } = useVendor();

  // 芒鈥濃偓芒鈥濃偓 Dialog state 芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓
  const [vendorDialog, setVendorDialog] = useState<{
    open: boolean;
    mode: 'add' | 'edit';
    vendor?: Vendor;
  }>({ open: false, mode: 'add' });

  const [deleteDialog, setDeleteDialog] = useState<{
    open: boolean;
    type: 'vendor';
    id?: number;
    name?: string;
  }>({ open: false, type: 'vendor' });

  // 芒鈥濃偓芒鈥濃偓 Load data 芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓
  useEffect(() => {
    fetchVendors();
  }, [fetchVendors]);

  // 芒鈥濃偓芒鈥濃偓 Filter data 芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓
  const filteredVendor = vendors.filter(
    o =>
      o.name.toLowerCase().includes(vendorSearch.toLowerCase())
  );



  // 芒鈥濃偓芒鈥濃偓 Handlers 芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓
  const openVendorAdd = () => setVendorDialog({ open: true, mode: 'add' });
  const openVendorEdit = (vendor: Vendor) => setVendorDialog({ open: true, mode: 'edit', vendor });

  const openDelete = (
    type: 'vendor',
    id: number,
    name: string
  ) => setDeleteDialog({ open: true, type, id, name });

  const closeAll = () => {
    setVendorDialog(prev => ({ ...prev, open: false }));
    setDeleteDialog({ open: false, type: 'vendor' });
  };

  const onSuccess = () => {
    closeAll();
    fetchVendors();
  };

  return (
    <div className="p-6 pt-20 space-y-8">
      <ContractGeneralHeader />

      <Tabs defaultValue="vendor">
        <ContractTabsList />

        {/* 芒鈥濃偓芒鈥濃偓 OFFICE TAB 芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓 */}
        <TabsContent value="vendor" className="space-y-6">
          <VendorTabContent
            data={filteredVendor}
            loading={VendorLoading}
            onAdd={openVendorAdd}
            onEdit={openVendorEdit}
            onDelete={(id, name) => openDelete('vendor', id, name)}
          />
        </TabsContent>

      </Tabs>

      {/* 芒鈥濃偓芒鈥濃偓 MODALS 芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓 */}
      <VendorEditModal
        open={vendorDialog.open}
        mode={vendorDialog.mode}
        vendor={vendorDialog.vendor}
        onOpenChange={open => !open && closeAll()}
        onSuccess={onSuccess}
      />

      {/* 芒鈥濃偓芒鈥濃偓 DELETE MODAL (Unified) 芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓芒鈥濃偓 */}

        <VendorDeleteModal
          open={deleteDialog.open}
          onOpenChange={open => !open && closeAll()}
          vendorId={deleteDialog.id}
          vendorName={deleteDialog.name}
        />
    </div>
  );
};

export default ContractManagement;




